    mocks.yield_receiver_to_any_instance_implementation_blocks = true
  end
end

shared_context 'tap stdout' do
  let(:output_io) { StringIO::new }
  let(:output) { output_io.string }
  before do
    class << $stdout
      attr_accessor :tap_io
      def puts *a
        tap_io.puts *a
        super *a
      end
    end
    $stdout.tap_io = output_io
  end
end

